    private void loadTicketData() {
    try {
        java.sql.Connection con = DBConnection.connect();
        String sql = "SELECT * FROM parking_tickets";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();
        
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 

        while (rs.next()) {
            
            Object[] row = {
                rs.getInt("ticket_id"),
                rs.getString("vehicle_plate"),
                rs.getString("entry_time"),
                rs.getInt("entry_teller_id"),
                rs.getString("exit_time"),     
                rs.getInt("exit_teller_id"),   
                rs.getString("ticket_status"),
                rs.getDouble("total_amount"),
                rs.getInt("floor_id")
            };
            model.addRow(row);
        }
        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error loading tickets: " + e);
        }
    }
    

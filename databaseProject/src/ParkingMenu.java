    private void startClock() {
        javax.swing.Timer timer;
        timer = new javax.swing.Timer(1000, e -> {
            SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Date.setText(sdf.format(new java.util.Date())); 
        });
        timer.start();
    }           
    
    private void refreshCounts() {
    try {
        java.sql.Connection con = DBConnection.connect();
        String sql = "SELECT floor_id, max_capacity, current_occupied FROM floors";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            int id = rs.getInt("floor_id");
            int max = rs.getInt("max_capacity");
            int current = rs.getInt("current_occupied");
            int available = max - current;


            if (id == 1) {
                jTextField4.setText(String.valueOf(current));     // Floor 1 Current
                jTextField7.setText(String.valueOf(available));   // Floor 1 Available
            } else if (id == 2) {
                jTextField5.setText(String.valueOf(current));     // Floor 2 Current
                jTextField8.setText(String.valueOf(available));   // Floor 2 Available
            } else if (id == 3) {
                jTextField6.setText(String.valueOf(current));     // Floor 3 Current
                jTextField9.setText(String.valueOf(available));   // Floor 3 Available
            }
        }
        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error loading counts: " + e);
    }
}

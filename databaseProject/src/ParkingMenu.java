    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String plate = jTextField10.getText();      // Plate Number Field
    String floorText = jTextField13.getText();  // Floor Level Field

    // Basic Check: Are fields empty?
    if(plate.isEmpty() || floorText.isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(null, "Please enter Plate Number and Floor!");
        return;
    }

    try {
        java.sql.Connection con = DBConnection.connect();

        java.sql.CallableStatement cst = con.prepareCall("{call sp_VehicleEntry(?, ?, ?)}");
        
        cst.setString(1, plate); 
        cst.setInt(2, Integer.parseInt(floorText));
        cst.setInt(3, Session.currentUserId);

        boolean hasResults = cst.execute();

        if(hasResults){
            java.sql.ResultSet rs = cst.getResultSet();
            
            if(rs.next()){
                String status = rs.getString("status");   // Get 'Success' or 'Error'
                String message = rs.getString("message"); // Get 'Ticket Created' or 'Floor is Full'
                
                if("Success".equalsIgnoreCase(status)){
                    javax.swing.JOptionPane.showMessageDialog(null, message);
 
                    jTextField10.setText("");
                    jTextField13.setText("");
                    
                    // Update the Dashboard numbers immediately
                    refreshCounts(); 
                    
                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, message, "Parking Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
        con.close();
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Entry Failed: " + e);
    }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

